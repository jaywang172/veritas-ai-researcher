def test_data_file():
    """Test if data file exists and is readable."""
    print("Testing Data File...")
    print("=" * 50)

    data_file = Path("sales_data.csv")
    if not data_file.exists():
        print(f"âŒ Data file not found: {data_file}")
        return False

    try:
        with open(data_file, "r", encoding="utf-8") as f:
            lines = f.readlines()

        print(f"âœ… Data file found: {data_file}")
        print(f"   - Lines: {len(lines)}")
        print(f"   - Size: {data_file.stat().st_size} bytes")

        # Show first few lines
        print("   - Preview:")
        for i, line in enumerate(lines[:3]):
            print(f"     {i+1}: {line.strip()}")

        return True

    except Exception as e:
        print(f"âŒ Error reading data file: {e}")
        return False


def test_dependencies():
    """Test if required dependencies are available."""
    print("Testing Dependencies...")
    print("=" * 50)

    required_packages = [
        "fastapi",
        "uvicorn",
        "crewai",
        "langchain_openai",
        "pandas",
        "matplotlib",
        "requests",
        "dotenv",
    ]

    missing = []
    for package in required_packages:
        try:
            if package == "dotenv":
                __import__("python_dotenv")
            elif package == "langchain_openai":
                __import__("langchain_openai")
            else:
                __import__(package.replace("-", "_"))
            print(f"âœ… {package}")
        except ImportError:
            print(f"âŒ {package}")
            missing.append(package)

    if missing:
        print(f"\nMissing packages: {', '.join(missing)}")
        print("Run: pip install -r requirements.txt")
        return False

    return True


